document.getElementById("show-more").addEventListener("click", Expand);
function Expand() {
  if (document.getElementById("attachment").style.display !== "block") {
    document.getElementById("attachment").style.display = "block";
    document.getElementById("add-inf").style.display = "block";
    document.getElementById("spacer").style.display = "block";
    document.getElementById("statut-title").style.textAlign = "center";
    document.getElementById("show-more-text").innerHTML = "Zwiń";
    document.getElementById("show-more-button").src =
      "../pub/assets/image/expand-less-rounded.svg";
  } else {
    document.getElementById("attachment").style.display = "none";
    document.getElementById("add-inf").style.display = "none";
    document.getElementById("spacer").style.display = "none";
    document.getElementById("statut-title").style.textAlign = "left";
    document.getElementById("show-more-text").innerHTML = "Rozwiń";
    document.getElementById("show-more-button").src =
      "../pub/assets/image/expand-more-rounded.svg";
  }
}
