document.getElementById("show-more").addEventListener("click", Expand);
function Expand() {
  if (document.getElementById("add-content").style.display !== "block") {
    document.getElementById("add-uchwale").style.display = "block";
    document.getElementById("add-content").style.display = "block";
    document.getElementById("spacer").style.display = "block";
    document.getElementById("uchwaly-title").style.textAlign = "center";
    document.getElementById("show-more-text").innerHTML = "Zwiń";
    document.getElementById("show-more-button").src =
      "../pub/assets/image/expand-less-rounded.svg";
  } else {
    document.getElementById("add-uchwale").style.display = "none";
    document.getElementById("add-content").style.display = "none";
    document.getElementById("spacer").style.display = "none";
    document.getElementById("uchwaly-title").style.textAlign = "left";
    document.getElementById("show-more-text").innerHTML = "Rozwiń";
    document.getElementById("show-more-button").src =
      "../pub/assets/image/expand-more-rounded.svg";
  }
}
