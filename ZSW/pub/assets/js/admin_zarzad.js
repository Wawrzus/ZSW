document.getElementById("show-more").addEventListener("click", Expand);
function Expand() {
  if (document.getElementById("form").style.display !== "block") {
    document.getElementById("form").style.display = "block";
    document.getElementById("rest-spacer").style.display = "block";
    document.getElementById("zarzad-title").style.textAlign = "center";
    document.getElementById("show-more-text").innerHTML = "Zwiń";
    document.getElementById("show-more-button").src =
      "../pub/assets/image/expand-less-rounded.svg";
  } else {
    document.getElementById("form").style.display = "none";
    document.getElementById("rest-spacer").style.display = "none";
    document.getElementById("zarzad-title").style.textAlign = "left";
    document.getElementById("show-more-text").innerHTML = "Rozwiń";
    document.getElementById("show-more-button").src =
      "../pub/assets/image/expand-more-rounded.svg";
  }
}
